package main

import (
	"flag"
)

// isFlagSet returns whether the specified command line flag has been set.
func isFlagSet(fs *flag.FlagSet, name string) bool {
	found := false
	fs.Visit(func(f *flag.Flag) {
		if f.Name == name {
			found = true
		}
	})
	return found
}
